package com.example.shoponline.model;

public class Vendor {
    String id, vid, title, image, image_cover, description,
            address, contact, chat_resp_time,  shipping_on_time,
            authentic_rating, days_return, warranty_period;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVid() {
        return vid;
    }

    public void setVid(String vid) {
        this.vid = vid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getImage_cover() {
        return image_cover;
    }

    public void setImage_cover(String image_cover) {
        this.image_cover = image_cover;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getChat_resp_time() {
        return chat_resp_time;
    }

    public void setChat_resp_time(String chat_resp_time) {
        this.chat_resp_time = chat_resp_time;
    }

    public String getShipping_on_time() {
        return shipping_on_time;
    }

    public void setShipping_on_time(String shipping_on_time) {
        this.shipping_on_time = shipping_on_time;
    }

    public String getAuthentic_rating() {
        return authentic_rating;
    }

    public void setAuthentic_rating(String authentic_rating) {
        this.authentic_rating = authentic_rating;
    }

    public String getDays_return() {
        return days_return;
    }

    public void setDays_return(String days_return) {
        this.days_return = days_return;
    }

    public String getWarranty_period() {
        return warranty_period;
    }

    public void setWarranty_period(String warranty_period) {
        this.warranty_period = warranty_period;
    }
}
